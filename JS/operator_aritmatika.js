var a = 10;
var b = 3;
var c = 0;

// pengurangan -
c = a - b;
console.log(`${a} - ${b} = ${c}`);

// perkalian *
c = a * b;
console.log(`${a} * ${b} = ${c}`);

// pemangkatan **
c = a ** b;
console.log(`${a} **${b} = ${c}`);

//pembagian /
c = a / b;
console.log(`${a} / ${b} = ${c}`);

//modulo %
c = a % b;
console.log(`${a} % ${b} = ${c}`);