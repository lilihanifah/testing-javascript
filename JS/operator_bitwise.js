var x = 10;
var y = 2;

// operator bitwise and &
var hasil = x & y;
console.log(`${x} & ${y} = ${hasil}`);

// operator bitwise or |
var hasil = x | y;
console.log(`${x} | ${y} = ${hasil}`);

// operator bitwise xor ^
var hasil = x ^ y ;
console.log(`${x} ^ ${y} = ${hasil}`);

// operator bitwise negasi ~
var hasil = ~x;
console.log(`~${x} = ${hasil}`);

// operator bitwise right shift >>
var hasil = x >> y;
console.log(`${x} >> $ ${y} = ${hasil}`);

// operator bitwise left shift <<
var hasil = x << y;
console.log(`${x} << ${y} = ${hasil}`);
