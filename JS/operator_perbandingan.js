var a = 30;
var b = 15;

// sama dengan ==
var hasil = a == b;
console.log(`${a} == ${b} = ${hasil}`);

// lebih besar >
var hasil = a > b;
console.log(`${a} > ${b} = ${hasil}`);

// lebih besar samadengan
var hasil = a >= b;
console.log(`${a} >= ${b} = ${hasil}`);

// lebih kecil
var hasil = a < b;
console.log(`${a} < $ {b} = ${hasil}`);

// lebih kecil samadengan
var hasil = a  <= b;
console.log(`${a} <= ${b} = ${hasil}`);

// tidak samadengan
var hasil = a != b;
console.log(`${a} != ${b} = ${hasil}`);